    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        DB::beginTransaction();
        try {
            $hapus = Pembelian::where('id_pembelian', $id)->first();
            $hapus->delete();

            $detail = PembelianDetail::where('id_pembelian', $id)->first();
            $detail->delete();
            DB::commit();
            Alert::success('Sukses', 'Data pembelian Berhasil Dihapus');
            return redirect(route('pembelian.index'));
        } catch (\Exception $e) {
            DB::rollBack();
            report($e);
            Alert::error('Gagal', 'Data pembelian Gagal Dihapus!');
            return back()->withInput();
        }
    }
}
